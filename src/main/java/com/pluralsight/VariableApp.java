package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {
        String favColor = "blue";
        String yearStarted = "2025";
        char middleInitial = 'G';
        Integer howManyPets = 1;
        String messageOrSaying = "Have a nice day!";
        System.out.println("My favorite color is "+favColor+".");
        System.out.println("The year I started this class was in "+yearStarted+".");
        System.out.println("My middle initial is "+middleInitial+".");
        System.out.println("I have a total of "+howManyPets+" but I work at an animal shelter so they're all technically my pets.");
        System.out.println("One thing I always say is:"+messageOrSaying);
    }
}
